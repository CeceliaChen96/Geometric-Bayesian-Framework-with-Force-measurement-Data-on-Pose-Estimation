"""Summarize paper-facing generated results from JSON records.

This script gives GitHub readers a Python entry point for inspecting the saved
experiment outputs without opening notebooks. It reads only files under
data/generated/ and writes compact Markdown/JSON summaries.
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

from common import ensure_summary_root, fmt, load_json, mean_std, write_json


RESULT_FILES = {
    "exp1_sparse_k": "data/generated/posterior_estimation/exp1_multiseed_nested_K_aggregate_records.json",
    "exp2a_noise_robustness": "data/generated/posterior_estimation/exp2A_noise_robustness_summary.json",
    "exp2b_noise_model_mismatch": "data/generated/posterior_estimation/exp2B_noise_model_mismatch_summary.json",
    "exp2c_prior_mismatch": "data/generated/posterior_estimation/exp2C_initialization_prior_mismatch_summary.json",
    "baseline_scheme_ab": "data/generated/baseline_experiments/scheme_ab_accuracy_summary.json",
    "diagnostics": "data/generated/diagnostics/chart_drift_diagnostic_summary.json",
}


def _best_by(rows: list[dict[str, Any]], key: str, higher_is_better: bool = True) -> dict[str, Any] | None:
    usable = [row for row in rows if isinstance(row.get(key), (int, float))]
    if not usable:
        return None
    return sorted(usable, key=lambda row: row[key], reverse=higher_is_better)[0]


def build_summary() -> dict[str, Any]:
    exp1 = load_json(RESULT_FILES["exp1_sparse_k"])
    exp2a = load_json(RESULT_FILES["exp2a_noise_robustness"])
    exp2b = load_json(RESULT_FILES["exp2b_noise_model_mismatch"])
    exp2c = load_json(RESULT_FILES["exp2c_prior_mismatch"])
    baseline = load_json(RESULT_FILES["baseline_scheme_ab"])
    diagnostics = load_json(RESULT_FILES["diagnostics"])

    exp1_selected = [row for row in exp1 if row.get("selected")]
    baseline_scheme_b = [row for row in baseline if row.get("scheme") == "B"]

    return {
        "source_files": RESULT_FILES,
        "exp1_sparse_k": {
            "n_rows": len(exp1),
            "K_values": [row.get("K") for row in exp1],
            "selected_rows": exp1_selected,
            "best_residual_reduction_row": _best_by(exp1, "rho_reduction_pct_mean"),
        },
        "exp2a_noise_robustness": {
            "n_rows": len(exp2a),
            "alpha_values": [row.get("alpha") for row in exp2a],
            "best_rot_improvement_row": _best_by(exp2a, "rot_imp_mean"),
            "best_trans_improvement_row": _best_by(exp2a, "trans_imp_mean"),
        },
        "exp2b_noise_model_mismatch": {
            "n_rows": len(exp2b),
            "alpha_values": [row.get("alpha") for row in exp2b],
            "best_rot_improvement_row": _best_by(exp2b, "rot_imp_mean"),
            "best_trans_improvement_row": _best_by(exp2b, "trans_imp_mean"),
        },
        "exp2c_prior_mismatch": {
            "n_rows": len(exp2c),
            "cases": [row.get("case") for row in exp2c],
            "rows": exp2c,
        },
        "baseline_scheme_b": {
            "n_rows": len(baseline_scheme_b),
            "rows": baseline_scheme_b,
            "best_rot_improvement_row": _best_by(baseline_scheme_b, "rot_improv_vs_init_pct_mean"),
            "best_trans_improvement_row": _best_by(baseline_scheme_b, "trans_improv_vs_init_pct_mean"),
        },
        "diagnostics": diagnostics,
    }


def render_markdown(summary: dict[str, Any]) -> str:
    lines: list[str] = [
        "# Python Summary of Generated Paper Results",
        "",
        "This file is generated by `python experiments/summarize_paper_results.py`.",
        "It summarizes saved generated records and does not rerun notebooks.",
        "",
        "## Source Files",
        "",
    ]
    for name, path in summary["source_files"].items():
        lines.append(f"- `{name}`: `{path}`")

    exp1 = summary["exp1_sparse_k"]
    best_exp1 = exp1["best_residual_reduction_row"] or {}
    lines += [
        "",
        "## Experiment 1: Sparse-K Sweep",
        "",
        f"- Rows: {exp1['n_rows']}",
        f"- K values: {', '.join(str(k) for k in exp1['K_values'])}",
        f"- Best residual reduction row: K={fmt(best_exp1.get('K'))}, "
        f"rho reduction={mean_std(best_exp1, 'rho_reduction_pct_mean', 'rho_reduction_pct_std')}%",
    ]

    for key, title in [
        ("exp2a_noise_robustness", "Experiment 2A: Noise Robustness"),
        ("exp2b_noise_model_mismatch", "Experiment 2B: Noise-Model Mismatch"),
    ]:
        item = summary[key]
        best_rot = item["best_rot_improvement_row"] or {}
        best_trans = item["best_trans_improvement_row"] or {}
        lines += [
            "",
            f"## {title}",
            "",
            f"- Rows: {item['n_rows']}",
            f"- Alpha values: {', '.join(str(a) for a in item['alpha_values'])}",
            f"- Best rotational improvement: alpha={fmt(best_rot.get('alpha'))}, "
            f"rot improvement={fmt(best_rot.get('rot_imp_mean'))}%",
            f"- Best translational improvement: alpha={fmt(best_trans.get('alpha'))}, "
            f"translation improvement={fmt(best_trans.get('trans_imp_mean'))}%",
        ]

    exp2c = summary["exp2c_prior_mismatch"]
    lines += [
        "",
        "## Experiment 2C: Prior-Mismatch Cases",
        "",
        "| Case | beta | c_kappa | c_Lambda | Alg1 eR | Alg2 eR | rho improvement (%) |",
        "|---|---:|---:|---:|---:|---:|---:|",
    ]
    for row in exp2c["rows"]:
        lines.append(
            "| "
            + " | ".join(
                [
                    str(row.get("case", "")),
                    fmt(row.get("beta")),
                    fmt(row.get("c_kappa")),
                    fmt(row.get("c_Lambda")),
                    mean_std(row, "Alg1_eR_mean", "Alg1_eR_std"),
                    mean_std(row, "Alg2_eR_mean", "Alg2_eR_std"),
                    fmt(row.get("rho_imp_mean")),
                ]
            )
            + " |"
        )

    baseline = summary["baseline_scheme_b"]
    lines += [
        "",
        "## Baseline Comparison: Scheme B",
        "",
        "| Method | K | Rot. improvement (%) | Trans. improvement (%) | Runtime (s) |",
        "|---|---:|---:|---:|---:|",
    ]
    for row in baseline["rows"]:
        lines.append(
            "| "
            + " | ".join(
                [
                    str(row.get("method", "")),
                    fmt(row.get("K")),
                    mean_std(row, "rot_improv_vs_init_pct_mean", "rot_improv_vs_init_pct_std"),
                    mean_std(row, "trans_improv_vs_init_pct_mean", "trans_improv_vs_init_pct_std"),
                    mean_std(row, "runtime_sec_mean", "runtime_sec_std"),
                ]
            )
            + " |"
        )

    lines += [
        "",
        "## Diagnostics",
        "",
        "The diagnostic summary is included in the companion JSON output because its",
        "schema is diagnostic-specific.",
        "",
    ]
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Summarize saved paper-result JSON records.")
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=ensure_summary_root(),
        help="Directory for summary outputs.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    summary = build_summary()
    output_dir = args.output_dir
    write_json(output_dir / "paper_results_summary.json", summary)
    markdown = render_markdown(summary)
    (output_dir / "paper_results_summary.md").write_text(markdown + "\n", encoding="utf-8")
    print(f"Wrote paper-result summaries to {output_dir}")


if __name__ == "__main__":
    main()

